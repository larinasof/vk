package ru.netology.domain;

public class SharedInfo {
    private int amountOfShares;
    private boolean hadShared;

    public int getAmountOfShares() {
        return amountOfShares;
    }

    public void setAmountOfShares(int amountOfShares) {
        this.amountOfShares = amountOfShares;
    }

    public boolean isHadShared() {
        return hadShared;
    }

    public void setHadShared(boolean hadShared) {
        this.hadShared = hadShared;
    }



}
